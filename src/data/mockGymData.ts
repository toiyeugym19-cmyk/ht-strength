/** Helper to generate check-in history entries */
function checkIns(daysAgo: number[], types: string[]): Array<{ date: string; type: string }> {
  return daysAgo.map((d, i) => ({
    date: new Date(Date.now() - d * 86400000).toISOString(),
    type: types[i % types.length] || 'Gym Access',
  }));
}

